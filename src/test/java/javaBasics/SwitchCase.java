package javaBasics;

public class SwitchCase {
	public static void main(String args[]) {
		
		int a = 9;
		switch(a) {
		
		case 1:
			System.out.println("i am in one");
			
		case 2:
			System.out.println("I am in two");
		break;
		 default: System.out.println("yet to  come to world");
			
		}
		
	}

}
